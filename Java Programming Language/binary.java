
public class binary {

    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        int sum = a+b;
        int difference = a-b;
        int product = a*b;
        int divide = a/b;
        int modulo = a%b;
        System.out.println(sum);
        System.out.println(difference);
        System.out.println(product);
        System.out.println(divide);
        System.out.println(modulo);
    }
    
}
